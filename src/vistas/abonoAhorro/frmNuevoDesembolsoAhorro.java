    @Override
    public void actionPerformed(ActionEvent e) {
        if(e.getSource() == btnGuardar){
            float resto, capital, desembolso;
        
            capital = Float.parseFloat(txtCapital.getText());
            desembolso = Float.parseFloat(txtEfectivo.getText());
            if(capital >= desembolso && txtEfectivo.getText().equals(txtEfectivo1.getText())){
                guardarDesembolso();
            }else{
                JOptionPane.showMessageDialog(null,"VALOR NO VALIDO");
            }
        }
        if(e.getSource()==btnCancelar){
            this.dispose();
        }
    }
    
    private void guardarDesembolso(){
        try{
            DesembolsoAhorro desembolso = new DesembolsoAhorro();
            
            desembolso.setId_ahorro(idAhorro);
            desembolso.setMonto(Float.parseFloat(txtEfectivo.getText()));
            
            funcion.agregarDesembolso(desembolso);
            
            JOptionPane.showMessageDialog(null,"Datos Guardados Correctamente");
            this.dispose();
            
       }catch(Exception e){
            //JOptionPane.showMessageDialog(null,"Error:"+e.getMessage()+"\nVerifiqueCarlos"); 
       }
    }
    
    private void consultarCapitalActual(){
        
        
        float monto = 0;
        
        monto = funcion.mostrarCapitalActual(idAhorro);
        txtCapital.setText(Float.toString(monto));
       
       
        if(monto == 0){
            txtEfectivo.setEnabled(false);
            txtEfectivo1.setEnabled(false);
            mensaje.setText("Ya se desembolsó el capital total de la cuenta");
        }
        
    }
    
    
    
