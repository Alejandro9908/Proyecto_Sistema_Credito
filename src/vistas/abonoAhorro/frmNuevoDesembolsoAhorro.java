    private void txtEfectivoCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtEfectivoCaretUpdate
        float resto, capital, desembolso;
        
        capital = Float.parseFloat(txtCapital.getText());
        desembolso = Float.parseFloat(txtEfectivo.getText());
        if(capital >= desembolso){
            resto = capital - desembolso;
            txtCapitalRestante.setText(Float.toString(resto));
        }else{
            txtCapitalRestante.setText("NO VALIDO");
        }
    }//GEN-LAST:event_txtEfectivoCaretUpdate


